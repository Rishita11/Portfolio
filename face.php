                        </h3>
                                <p>
                                 The face detector proposed  used <span style="font-weight: bold ;">Haar-Like features </span>cascaded classifiers, which achieve good performance with real-time efficiency. However, a few works show that Haar-Like feature may degrade considerably in real world applications with larger visual variations of human faces even with more advanced features and more training images. 
                                </p>
                                <p>
                                 <span style="font-weight: bold ;">Local Binary Pattern Histogram (LBP)  </span>is a simple yet very efficient texture operator which labels the pixels of an image by thresholding the neighborhood of each pixel and considers the result as a binary number.
                                It is a powerful feature for texture classification. It has further been determined that when LBP is combined with histograms of oriented gradients (HOG) descriptor, it improves the detection performance considerably on some datasets.</p>

                                <p>Using the LBPH combined with histograms we can represent the face images with a simple data vector. As LBPH is a visual descriptor it can also be used for face recognition tasks.</p>
                                
                        
